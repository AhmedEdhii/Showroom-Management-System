    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try {
            ps = conn.prepareStatement("select * from employees where Employee_id = ?");
            int employee_id = Integer.parseInt(txtemployeeid.getText());
            ps.setInt(1, employee_id);
            rs = ps.executeQuery();
            if (rs.next() == true) {
                txtname.setText(rs.getString(2));
                txtphonenumber.setText(rs.getString(3));
                txtaddress.setText(rs.getString(4));
                txtsalary.setText(Integer.toString(rs.getInt(5)));
                txtpassword.setText(rs.getString(6));
                txtcarssold.setText(Integer.toString(rs.getInt(7)));
                txtcomission.setText(Integer.toString(rs.getInt(8)));
                txtdepartmentid.setText(Integer.toString(rs.getInt(9)));
                txtname.requestFocus();
            } else {
                JOptionPane.showMessageDialog(this, "Not Avalaible");
                txtemployeeid.setText("");
                txtemployeeid.requestFocus();
            }
        } catch (SQLException ex) {
            Logger.getLogger(employeeRecord.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

